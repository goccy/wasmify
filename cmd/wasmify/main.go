func main() {
	// Handle wrapper mode: if invoked as a compiler symlink, act as wrapper
	if wrapper.IsWrapperInvocation() {
		if err := wrapper.RunAsWrapper(); err != nil {
			fmt.Fprintf(os.Stderr, "wasmify wrapper error: %v\n", err)
			os.Exit(1)
		}
		return
	}

	code := dispatch(os.Args[1:])
	if code != 0 {
		os.Exit(code)
	}
}

// dispatch routes subcommand arguments to the appropriate handler. Returns
// the process exit code (0 on success).
func dispatch(argv []string) int {
	// --non-interactive is global. Strip it up front so every
	// subcommand gets the clean argv but the rest of the process knows
	// to fail-fast on any prompt path.
	argv = stripNonInteractiveFlag(argv)

	if len(argv) < 1 {
		printUsage()
		return 1
	}

	subcmd := argv[0]
	args := argv[1:]

	var err error
	switch subcmd {
	case "init":
		err = cmdInit(args)
	case "status":
		err = cmdStatus(args)
	case "save-arch":
		err = cmdSaveArch(args)
	case "classify":
		err = cmdClassify(args)
	case "build":
		err = cmdBuild(args)
	case "generate-build":
		err = cmdGenerateBuild(args)
	case "validate-build":
		err = cmdValidateBuild(args)
	case "install-sdk":
		err = cmdInstallSDK(args)
	case "ensure-tools":
		err = cmdEnsureTools(args)
	case "wasm-build":
		err = cmdWasmBuild(args)
	case "parse-headers":
		err = cmdParseHeaders(args)
	case "gen-proto":
		err = cmdGenProto(args)
	case "optimize":
		err = cmdOptimize(args)
	case "update":
		err = cmdUpdate(args)
	case "help", "-h", "--help":
		printUsage()
	default:
		fmt.Fprintf(os.Stderr, "unknown command: %s\n", subcmd)
		printUsage()
		return 1
	}

	if err != nil {
		fmt.Fprintf(os.Stderr, "error: %v\n", err)
		return 1
	}
	return 0
}

